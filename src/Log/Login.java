    void Logearse() throws SQLException{
        usuario = jTextUsuario.getText();
        pass = jPasswordContraseniausuario.getText();
       String url = "select * from Usuarios where Nombre_Usuario = '"+usuario+"' and Password = '"+pass+"'";
        SQLServerDataSource con = new SQLServerDataSource();
        con.setServerName("localhost");
        con.setDatabaseName("Gestion4F");//nombre de mi base de datos 
        con.setUser("4FAdmin");
        con.setPassword("CuatroF");
        con.setPortNumber(1433);
        
      try {
          Connection cn = con.getConnection();
          PreparedStatement ps = cn.prepareStatement(url);
          ResultSet rs = ps.executeQuery();

          if(rs.next()){
              
              String u = rs.getString("Nombre_Usuario");
              String p = rs.getString("Password");
              String rol = rs.getString("Rol_Usuario");
              if(pass.equals(p))
              {
                  //Menu Principal de Administradores
                  if(rol.equals("1")){ 
                      JOptionPane.showMessageDialog(null,"Bienvenido: " + usuario);
                      new MAdministrador(u).setVisible(true);
                      this.setVisible(false);
                      GENH(u);
        //              usuario(u);
                        
                  }
                  //Menu Principal de Clientes
                  if(rol.equals("2")){
                      JOptionPane.showMessageDialog(null,"Bienvenido: " + usuario);
                      new MEmpleado(usuario).setVisible(true);
                      this.setVisible(false);
                      GENH(u);
                        
      //                usuario(u);
                  }
                  if(rol.equals("3"))
                  {
                      JOptionPane.showMessageDialog(null,"Bienvenido: " + usuario);
                      new MRoberta(usuario).setVisible(true);
                      this.setVisible(false);
                      GENH(u);
                      
                  }
              
              } else {
                  JOptionPane.showMessageDialog(null,"USUARIO o CONTRASEÃ‘A INCORRECTA");
              }
          } else {
              JOptionPane.showMessageDialog(null,"EL USUARIO NO EXISTE");
              
          }
          
          } catch (SQLException ex) {
          //JOptionPane.showMessageDialog(null,ex.toString());
      }
      
    }
    
    
