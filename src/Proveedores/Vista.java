private void cf() throws SQLException
{
int consulta;
String b;
b=var.getGetTexto();
consulta=Integer.parseInt(b);
        java.sql.Statement stm=Conexion.getConexion().createStatement();
        ResultSet rst=stm.executeQuery("select NumProveedor"
        + ",Nombre, "
        + "RFC, "
        + "Nombre_Calle, "
        + "Num_Int, "
        + "Num_Ext, "
        + "Estado, "
        + "Municipio, "
        + "Colonia, "
        + "CP, "
        + "Telefono, "
        + "email "
        + "FROM Proveedores inner join DomProveedores on Proveedores.NumProveedor=DomProveedores.Num_Proveedor "
        + "where NumProveedor like '%"+b+"%'");
        while(rst.next())
        {
        ID=rst.getString(1);
        NOMBRE=rst.getString(2);
        RFC=rst.getString(3);
        NUMI=rst.getString(4);
        NUME=rst.getString(5);
        MUNICIPIO=rst.getString(6);
        NOMCALLE=rst.getString(7);
        COLONIA=rst.getString(8);
        ESTADO=rst.getString(9);
        CODPOS=rst.getString(10);
        TELEFONO=rst.getString(11);
        CORREO=rst.getString(12);
        
            
        txtID.setText(ID.trim());
        txtNom.setText(NOMBRE.trim());
        txtRFC.setText(RFC.trim());
        txtNI.setText(NUMI.trim());
        txtNE.setText(NUME.trim());
        txtMUN.setText(MUNICIPIO.trim());
        txtNC.setText(NOMCALLE.trim());
        txtCOL.setText(COLONIA.trim());
        txtEST.setText(ESTADO.trim());
        txtCP.setText(CODPOS.trim());
        txtTEL.setText(TELEFONO.trim());
        txtCE.setText(CORREO.trim());
        }   

}
